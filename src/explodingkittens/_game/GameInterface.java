package explodingkittens._game;

/**
 * Contains unimplemented methods which
 * both online and offline games share.
 */
public interface GameInterface {
    /**
     * Unimplemented method that stands for game match players logic.
     */
    void matchPlayersUp();

    /**
     * Unimplemented method that stands for the game start logic.
     */
    void startUp();
}
